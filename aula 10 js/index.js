console.log(num1 === num2);//true

let num3 = 10;
let num4 = "10";
console.log(num3 === num4)//false, pois === compara valor e o tipo (se é String u Number por exemplo)
console.log(num3 != num4);//false, pois != compara apenas o valor e IGNORA o tipo (se é String u Number por exemplo)
console.log(num3 !== num4);//true, pois !== compara valor e o tipo (se é String u Number por exemplo)



